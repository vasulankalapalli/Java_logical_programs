//20. Write a JAVAprogram to print all even numbers between 1 to 100. - using while     loop

class Even 
{
    public static void main(String args[])
    {
        int i;
        for(i=0;i<=100;i++)
        {
            if(i%2==0)
            {
                System.out.println(i);
            }
        }
    }

}
